//Print all the alphabets from A to Z using ASCII code.

public class PQ2 {
    public static void main(String[] args) {
        System.out.print("Lower case : ");
        for(char i=65; i<=90; i++){
            System.out.print(i+" ");
        }
        System.out.println("");
        System.out.print("Upper case : ");
        for(char i=97; i<=122; i++){
            System.out.print(i+" ");
        }
    }
}
